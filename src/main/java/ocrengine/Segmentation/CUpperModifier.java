package ocrengine.Segmentation;

import java.util.*;

public class CUpperModifier {
    int upperModifierStart;
    int upperModifierEnd;
    
    public void setUpperModifierStart (int a_UpperModifierStart)
    {
        upperModifierStart = a_UpperModifierStart;
    }
    
    public void setUpperModifierEnd(int a_UpperModifierEnd)
    {
        upperModifierEnd = a_UpperModifierEnd;
    }
    
    public int getUpperModifierStart ()
    {
        return upperModifierStart;
    }
    
    public int  getUpperModifierEnd()
    {
        return upperModifierEnd;
    }  
}